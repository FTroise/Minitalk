#include "minitalk.h"

int	ft_atoi(char *s)
{
	int	i;
	int	segno;
	int	ris;

	i = 0;
	segno = 1;
	ris = 0;
	while (s[i])
	{
		if ((s[i] <= 32) || (s[i] == '+'))
			i++;
		if (s[i] == '-')
			segno = -1;
		if (s[i] >= '0' && s[i] <= '9')
		{
			ris *= 10;
			ris += s[i] - '0';
		}
		i++;
	}
	return (segno * ris);
}
